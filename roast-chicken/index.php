			<h2>Prep</h2>
			<p>Put the <span class="item-1">sugar</span>, the <span class="item-3">thyme</span>, and the <span class="item-2">salt</span> into 500ml of water. Slowly heat the water, and keep stirring until the salt and the suger have dissolved. Now take the pan of the heat, add 1,5 liters of cold water and put it in the fridge. The brine should be cold before you put the chicken into its bath.</p>

			<p>When the brine is cold, clean the <span class="item-4">chicken</span>, put it into a plastic bag and pour brine into it. Close the bag, with as little air as possible, and leave it in the fridge for 4—8 hours. You can turn the bag every now and then if you get bored.</p>

			<p>Grind some <span class="item-3">thyme</span> until it’s powdery. Mix the <span class="item-6">oil</span> and the <span class="item-7">wine</span> and whip it until it’s schloppy. Right before you pour this mixture over the chicken, you add the powdery thyme to it. Not yet.</p>

			<p>Clean the <span class="item-4">chicken</span> <em>again</em>, rub the inside of the chicken in with a mixture of <span class="item-3">thyme</span> and <span class="item-9">pepper</span>, and put the <span class="item-5">cloves of garlic</span> in it. Truss the chicken firmly and thread the chicken onto the rotisserie rod.</p>

			<h2>Cooking</h2>
			<p>Turn on the grill. Put the rotisserie (indeed, with the chicken on it) in place. Now add the <span class="item-3">thyme</span> to the mixture of wine and oil and pour it over the rotating chicken. Repeat this every ten minutes.</p>

			<p>Squeeze out the juice from the <span class="item-8">lemon</span>, dissolve quite some <span class="item-10">salt</span> in it. Pour this over the chicken twenty minutes before it’s done. When the chicken is done, don’t throw away the drippings, you want to use it for the <a href="../chicken-broth/">chicken broth</a>.</p>

			<p>How long the chicken should be grilled depends on its weight. Seventy minutes is enough in my oven, with a chicken of 1.2 to 1.4 kilos.</p>

