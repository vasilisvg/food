			<p>Put <span class="item-1">olive oil</span> in a pan over medium heat. When it’s hot, add the <span class="item-2">onions</span>.</p>
			<p>After six minutes or so add the <span class="item-3">peppers</span>, the <span class="item-4">garlic</span>, the <span class="item-5">paprika powders</span> and the <span class="item-7">parsley</span>. <em>Stir regularly</em>. </p>
			<p>After stirring for about six minutes, add the <span class="item-8">broth</span>, the <span class="item-9">blended tomatoes</span>, and the <span class="item-11">oregano</span>. Add some pepper and salt, put on the lid and let it simmer for 20 minutes.</p>
			<p><em>It smells nice.</em></p>
			<p>Wash the <span class="item-12">beans</span> and add them. Let it simmer for another 10 minutes without the lid. It shouldn’t need extra water, but if it does, indeed add some.</p>
			<p>Put the lid back on, turn off the heat, and let it rest for 20 minutes. Which is not enough to cook <span class="item-14">rice</span>, so you probably want to start that a bit earlier. Serve the rice with the chili sin carne (and, for the non vegans: with a spoonful of <span class="item-13">soured cream</span> on top)</p>
